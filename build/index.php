                                             - Be introduced to two scenarios of unauthorized data transfer to investigate<br>
                                             - Be introduced to where and how different cloud file storage applications log <br>
                                             - Learn how to examine incidents with suspected data exfiltration using corporate issued and person cloud file storage use<br><br>

                                             The slides and labs will take a deep dive into Microsoft OneDrive, Google Drive, Dropbox, Box, and Citrix ShareFile to first understand what is known about the applications and artifacts left behind, then move into hands-on labs to analyze registry keys, log files, and other traces left behind by the applications.<br></p>
