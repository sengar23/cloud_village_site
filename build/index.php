                                <div class="panel-heading" role="tab" id="headingOne">
                                    <h4 class="panel-title">IAM Concerned: OAuth Token Hijacking in Google Cloud (GCP)</h4>
                                    <div class="plus-sym"></div>
                                </div>
                            </a>
                            <div id="collapseJenko" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
                                <div class="panel-body">
                                    <div class="item">
                                        <div class="services__item">
                                            <p class="purple"><strong class="white">Speaker:&nbsp;</strong><strong>Jenko Hwong</strong></p>
                                            <!--<p class="purple"><strong class="white">Date:&nbsp;</strong><strong>TBA</strong></p><p class="purple"><strong class="white">Time:&nbsp;</strong><strong>TBA</strong></p>-->
                                            <p class="purple"><strong class="white">Twitter:&nbsp;</strong><a href="https://twitter.com/jenkohwong" target="_blank"><strong>@jenkohwong</strong></a></p>
                                            <p class="white"><strong class="white">Abstract:&nbsp;</strong></p>
                                            <p class="services__item__text">Imagine you've protected your production Google Cloud environment from compromised credentials, using MFA and a hardware security key. However, you find that your GCP environment has been breached through hijacking of OAuth session tokens cached by gcloud access. Tokens were exfiltrated and used to invoke API calls from another host. The tokens were refreshed by the attacker and did not require MFA. Detecting the breach via Strackdriver was confusing, slowing incident response. And revoking the active OAuth sessions required finding OAuth tokens from logs and using a REST API call, causing further delays in remediation.<br><br>

                                             This talk will demonstrate a compromised credential attack in Google Cloud Platform by:<br><br>
                                             - hijacking cached OAuth tokens stored on a GCP administrator's client machine and<br>
                                             - reusing existing gcloud CLI sessions to gain access to multiple GCP environments<br>
                                             - showing that MFA does not apply to OAuth token refreshes for cached credentials (only the initial login)<br><br>

                                             The POC takes advantage of several issues with GCP IAM design or configuration:
                                             OAuth tokens are cached and unencrypted, allowing easy access once the client endpoint has been exploited.<br><br>
                                             - Tokens can have long or no expiration, allowing potentially long time windows for compromise.<br>
                                             - The attacker can easily refresh tokens, allowing persistence.<br>
                                             - Token refresh does not require MFA making it easy to maintain persistence, creating a false sense of security when MFA is enabled.<br>
                                             - Authentication and Access policies are defined in different admin areas, are confusing, and easily misconfigured.<br>
                                             - Configuring Stackdriver Logging is confusing, leading to slow or ineffective incident response.<br>
                                             - OAuth tokens cannot be revoked easily making remediation difficult.<br><br>

                                             We will discuss various approaches and challenges to defending:<br><br>
                                             1. Prevention<br><br>
                                             - MFA is not required to refresh the OAuth token<br>
                                             - Google cloud session timeout (GSuite Admin)<br>
                                             - IP whitelisting (using VPC Service Controls and Access Context Manager)<br>
                                             - Explicit client-side revokes (manual)<br>
                                             2. Detection <br><br>
                                             - Stackdriver logging data access events must be enabled for all services or else the abuse of OAuth tokens will not be logged and remediation will not be possible.<br>
                                             - Periodic audit checks on the logs or IAM configurations can be somewhat useful for compliance, but are not real-time so are of limited use for detection.<br>
                                             3. Remediation<br><br>
                                             - OAuth tokens can be revoked, but there are caveats:<br>
                                                + ""gcloud auth revoke"" only works on the compromised user's endpoint and requires the user account in order to look up the locally cached OAuth token. This will fail if the attacker deletes the gcloud credential cache.<br>
                                                + A REST API revoke call works and requires the OAuth token, so reliable logging and event parsing must be implemented to ensure tokens can be extracted quickly for IR.<br>
                                             - Deletion of user accounts has a huge impact.<br>
                                             - Browser sessions can be revoked but does not apply to Google Cloud sessions.</p>
                                            
